
	<h2>Mi ez az oldal?</h2>


	<h4>Az alapgondolat</h4>
	<blockquote>
		Több országban is teljesen normális és megszokott, hogy a diákok az interneten értékelhetik a tanáraikat.
		Magyarországon ez egyelőre csak a felsőoktatásban vált szokássá, melynek a 
		<a href='https://www.markmyprofessor.com/' target=_blank>markmyprofessor.com</a>
		weboldal biztosít teret.<br>
		A <a href='https://tanarvagyok.hu/'>tanarvagyok.hu</a> azt szeretné elősegíteni, hogy a tanárok munkájának 
		értékelése a diákok részéről nálunk is a közoktatás egy elfogadott és hasznos elemévé válhasson.
	</blockquote>


	<h4>A probléma</h4>
	<blockquote>
		Mivel Magyarországon ez a szemlélet még nagyon idegen, egy olyan weboldal, amely a tanárok szélesebb körű 
		és nyilvános értékelését tenné lehetővé az érintettek tudta és beleegyezése nélkül, jó eséllyel komoly 
		szakmai ellenállást válthatna ki a pedagógusok részéről. A kezdeményezés sikere érdekében ezt a teljesen 
		nyílt változatot mindenképpen érdemes elkerülni, és a kérdést másképpen megközelíteni.
	</blockquote>


	<h4>A megoldás</h4>
	<blockquote>
		A weboldal kialakításának alapgondolata, hogy bármely pedagógus értékelését kizárólag az érintett személy
		kezdeményezheti - rajta kívül ezt senki más nem teheti meg. Az értékelés kezdeményezése regisztrációhoz 
		kötött. A regisztrációt követően a pedagógus a belső felületen generálhat egy linket a maga számára,
		mely linket megosztva a tanulói, illetve diákcsoportjai között ők elvégezhetik az értékelést. A
		kapott eredmények összesítését elsőként csak az érintett pedagógus láthatja, ám ha az értékelés lezárását
		követően úgy gondolja, hogy annak tartalmát szeretné korlátozottan (a kitöltők számára), vagy nyilvánosan 
		(a weboldal felhasználói számára) megosztani, akkor ezt megteheti.<br>
		Egy pedagógus több csoporttal is értékeltetheti magát. Egy-egy értékelés akkor érvényes, ha azon belül 
		legalább hat tanulótól érkezett visszajelzés. A különböző csoportok értékelései nem vonódnak össze, azok 
		mindegyike önálló értékelésnek számít. Ennek megfelelően minden csoport összesített értékelése önmagában
		tehető közzé a felületen. Bármely csoport korábban közzétett értékelésének megosztása bármikor visszavonható.
	</blockquote>


	<h4>A cél</h4>
	<blockquote>
		A kezdeményezés elsődleges célja, hogy a tanárok anonim - és ezáltal valid - visszajelzést, értékelést 
		kaphassanak tanulóiktól saját pedagógiai, oktatói munkájukra vonatkozóan. A projekt hosszabb távon azonban
		arra is lehetőséget biztosíthat, hogy az értékelések figyelembe vételével ekképpen is elismerhetővé váljon
		a tartósan színvonalas, kiemelkedő pedagógiai tevékenység.
	</blockquote>


